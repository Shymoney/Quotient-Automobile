<h3> What Is This Website/Concept About?</h3>
<p>Gomyway is a ridesharing platform connecting people who are driving and have spare car seats to share with peope who need rides and are going in the same direction. It helps people reduce their travel.commute cost as well as provides a more convenient ride. Ride sharing concept can help reduce traffic congestion, protect our environment by reducing air pollution and thereby, improving our health. The benefits are immeasurable. For more about us, read here</p>

<h3>How Do I Know That I Can Trust Another Member?</h3>
<p>Members’ identities are first being verified through our customized 4-level verification process which include: email, phone number, Facebook and valid ID to ensure that people behave responsibly. With Facebook verification, members can link their GoMyWay profile to their existing social network, increasing trust amongst members as you can go ahead and view a members’ profile page.
When two members meet in real life to rideshare, they publicly rate each other. Therefore you can view the ratings about a member before sharing a ride to instill your trust in that person.</p>

<h3>Why Do I Have To Sign Up With Facebook?</h3>
<p>Signing up with Facebook makes your profile more trustworthy, as it links it to your existing online social profile, which is reassuring to other members. Your Facebook details will automatically complete your profile, including your photo and your number of friends. The more complete your profile is, the more trustworthy it will be.</p>

<h3>I Would Like To Offer A Recurring Ride For My Daily Commute</h3>
<p>We are currently working on a recurring ride feature. In the meantime, you can re-offer rides easily. Go to “rides offered” to view your list of past rides, then click on the “offer again” link to re-offer a ride. Takes 10 seconds or less to do.</p>

<h3>How Do I Find Out More About Someone Who Wants To Join My Ride?</h3>
<p>When a co-traveller sends you a private message requesting to join your ride, you can go to their profile by opening the message and then clicking on their name.  On their profile, you will be able to see their age, rating, bio, level of verification and preferences. It is also likely you will find more information such as their occupation, marital status and reasons for travelling in their bio.</p>

<h3>Can I Decline A Ride Request Due To Personal Reasons?</h3>
<p>As a ride owner, you have the choice of accepting or declining a request by another member to join your ride if you do not feel comfortable with the person’s details or level of verification.</p>


<h3>How Do I Get Paid?</h3>
<p>Payment is made in cash by your co-traveller(s) at the time of the trip. Each co-traveller will pay you the “price per passenger” amount that you stated when offering your ride. The co-traveller is expected bring the exact cash amount but it is advisable to always have change on you in case they haven't. We recommend that you ask the co-traveller for payment directly at the point of departure.</p>

<h3>How Do Pricing And Payment Work?</h3>
<p>When a car owner offers a ride, the prices are fixed, non-negotiable and the same for all co-travellers. The price is always based on a suggestion calculated using the GoMyWay formula which takes into consideration the real costs incurred by car owners. Car owners are free to adjust the suggested price within reason, to account for the comfort of their car. The price cannot exceed a maximum limit set by GoMyWay, in order to ensure costs are fairly shared and that car owners do not make a profit (This is Not a taxi service). Generally, price per passenger represents a third of the fuel cost for the journey; when a car owner takes three co-travellers, he can offset all fuel costs.</p>

<h3>I Can’t Find The Ride I Want To Join, What Can I Do?</h3>
<p>We are sorry about that but no worries, kindly click on the “Create email alert” button on the search result page so you can be notified as soon as the ride you’re looking for is available.</p>

<h3>How Can I Find Out More About A Car Owner?</h3>
<p>When you find a ride, you can get more information about a car owner by going to their profile - simply click on their name on the ride details page. On their profile, you will be able to see their age, rating, bio, level of verification and preferences. It is also likely you will find more information such as their occupation, marital status and reasons for travelling in their bio.</p>

<h3>How Do I Join Someone’s Ride?</h3>
<p>To join a ride, all you need to do is click the book  a seat button on the page containing details about the ride. You can either contact the ride owner by sending a private message (recommended) or via a phone call or both.</p>

<h3>Can I Both Offer And Join A Ride?</h3>
<p>As a member, you can offer a ride today and you can also decide to join another member’s ride the next time if you would like to.</p>

<h3>When I Share A Ride, Will My Valid Id Be Visible?</h3>
<p>No, your valid ID is confidential to us as GoMyWay would not disclose your valid ID. Your valid ID is solely meant to ensure the safety of others.</p>

<h3>What Do I Do When I Find A Suspicious User/ride?</h3>
<p>If you find a suspicious user/ride you can always let us know about it by clicking on the ‘Report this user’ text beside the member’s name or “Report this ride” text on the ride detail’s page.</p>

<h3>I Am Interested In This Concept, How Can I Help Out?</h3>
<p>Thank you! All you have to do is click on the social media buttons right at the end of the page to tell your friends about it.</p>

<h3>When Should I Leave A Rating And What About?</h3>
<p>It's important to leave a rating after travelling with a member describing your experience and telling the community whether or not you recommend this member. If with hindsight, you would be happy to share a car again, leave a positive rating. If not, leave a negative rating but with good and valid reason.</p>

